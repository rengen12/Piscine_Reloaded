int		ft_count_if(char **tab, int (*f)(char*))
{
	int		c;
	int		i;

	c = 0;
	i = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			c++;
		i++;
	}
	return (c);
}
